#Дано число A (>1). Вывести наибольшее из целых чисел K, для которых сумма 1 +
#1/2 + ... + 1/K будет меньше A, и саму эту сумму.

try:
    a = float(input("Введите число A (>1): "))

    # Проверка условия
    if a <= 1:
        print("Ошибка: A должно быть больше 1")
    else:
        s = 0.0   # поле для текущей суммы
        k = 0     # счётчик членов ряда

        # Пока сумма меньше a, добавляем следующее значение
        while s + 1 / (k + 1) < a:
            k += 1
            s += 1 / k

        print(f"Наибольшее целое K = {k}")
        print(f"Сумма = {s}")
except ValueError:
    print("Ошибка, введено не число")
